// Generic dialog. Takes more specific dialog contents in props.
// Prepends anything passed in the child element with the props.title and props.message.
function Dialog(props) {
  return (
    <FancyBorder color="blue">
      <h1 className="Dialog-title">
        {props.title}
      </h1>
      <p className="Dialog-message">
        {props.message}
      </p>
      {props.children}
    </FancyBorder>
  );
}
